/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 * 题干有点没有描述清楚，这题目主要是找到两个链表的交汇节点，并返回交汇之后的节点
 * 例如[3,4,5,6,7] 和[9,5,6,7]  则返回 [5,6,7]
 * 首先需要将两个链表的长度找平，使用循环的算法 节点1：a+c 节点2 ：b+c
 * 则满足a+c+b+c = a+c+b+c ，会在两个链表村换到第一个重复节点重合或空节点重合
 * 然后判断重合节点，如果是null 则认为无交集  如果是某个节点则可直接返回
 *
 * 执行耗时:1 ms,击败了99.97% 的Java用户
 * 内存消耗:42.5 MB,击败了78.57% 的Java用户
 */
public class Solution {
    public ListNode getIntersectionNode(ListNode headA, ListNode headB) {
        if (headA == null || headB == null) return null;
        ListNode pA = headA, pB = headB;
        // 在这里第一轮体现在pA和pB第一次到达尾部会移向另一链表的表头,
        // 而第二轮体现在如果pA或pB相交就返回交点, 不相交最后就是null==null
        while (pA != pB) {
            pA = pA == null ? headB : pA.next;
            pB = pB == null ? headA : pB.next;
        }
        return pA;
    }
}
